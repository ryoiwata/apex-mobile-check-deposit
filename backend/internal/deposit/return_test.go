// TestReturn_BankReferenceAutoGenerated verifies that the Return handler generates
// a bank_reference when none is provided in the request body.
func TestReturn_BankReferenceAutoGenerated(t *testing.T) {
	db := getReturnTestDB(t)
	defer db.Close()

	id := insertCompletedTransfer(t, db, 100000)
	defer cleanupReturnTransfer(t, db, id)

	gin.SetMode(gin.TestMode)
	r := gin.New()
	svc := NewService(db, state.New(db), nil, nil, ledger.NewService(db))
	handler := NewHandler(svc, Config{ReturnFeeCents: 3000}, notification.NewRepo(db))
	r.POST("/deposits/:id/return", handler.Return)

	body, _ := json.Marshal(map[string]string{
		"reason_code": "stop_payment",
		// bank_reference intentionally omitted
	})
	req := httptest.NewRequest(http.MethodPost, "/deposits/"+id.String()+"/return", bytes.NewReader(body))
	req.Header.Set("Content-Type", "application/json")
	w := httptest.NewRecorder()
	r.ServeHTTP(w, req)

	require.Equal(t, http.StatusOK, w.Code)

	var resp map[string]any
	require.NoError(t, json.Unmarshal(w.Body.Bytes(), &resp))
	data := resp["data"].(map[string]any)
	bankRef, ok := data["bank_reference"].(string)
	require.True(t, ok, "bank_reference must be a string")
	assert.NotEmpty(t, bankRef, "bank_reference must be auto-generated when not provided")
	assert.Contains(t, bankRef, "RET-", "auto-generated bank reference should start with RET-")
}

