// getReturnTestDB opens a Postgres connection for return tests, skipping if unavailable.
func getReturnTestDB(t *testing.T) *sql.DB {
	t.Helper()
	dsn := os.Getenv("DATABASE_URL")
	if dsn == "" {
		dsn = "postgres://mcd:mcd@localhost:5432/mcd?sslmode=disable"
	}
	db, err := sql.Open("postgres", dsn)
	if err != nil {
		t.Skipf("skipping return test: cannot open postgres: %v", err)
	}
	ctx, cancel := context.WithTimeout(context.Background(), 3*time.Second)
	defer cancel()
	if err := db.PingContext(ctx); err != nil {
		db.Close()
		t.Skipf("skipping return test: postgres not reachable: %v", err)
	}
	return db
}

// insertCompletedTransfer inserts a transfer in completed state for return testing.
func insertCompletedTransfer(t *testing.T, db *sql.DB, amountCents int64) uuid.UUID {
	t.Helper()
	id := uuid.New()
	_, err := db.ExecContext(context.Background(), `
		INSERT INTO transfers
			(id, account_id, amount_cents, declared_amount_cents, status)
		VALUES ($1, $2, $3, $4, 'completed')`,
		id, "ACC-SOFI-1006", amountCents, amountCents)
	require.NoError(t, err, "insertCompletedTransfer")
	return id
}

// cleanupReturnTransfer removes a test transfer and all related rows.
func cleanupReturnTransfer(t *testing.T, db *sql.DB, id uuid.UUID) {
	t.Helper()
	db.ExecContext(context.Background(), `DELETE FROM notifications WHERE transfer_id = $1`, id.String())
	db.ExecContext(context.Background(), `DELETE FROM ledger_entries WHERE transfer_id = $1`, id)
	db.ExecContext(context.Background(), `DELETE FROM state_transitions WHERE transfer_id = $1`, id)
	db.ExecContext(context.Background(), `DELETE FROM transfers WHERE id = $1`, id)
}

