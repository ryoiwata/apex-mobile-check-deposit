// TestReturn_InsufficientFunds_ReversalAndFeePosted verifies that returning a deposit
// with "insufficient_funds" transitions the transfer to returned and posts reversal entries.
func TestReturn_InsufficientFunds_ReversalAndFeePosted(t *testing.T) {
	db := getReturnTestDB(t)
	defer db.Close()

	id := insertCompletedTransfer(t, db, 150000)
	defer cleanupReturnTransfer(t, db, id)

	svc := NewService(db, state.New(db), nil, nil, ledger.NewService(db))
	transfer, err := svc.ProcessReturn(context.Background(), id, "insufficient_funds", 3000)
	require.NoError(t, err)
	assert.Equal(t, models.StatusReturned, transfer.Status)

	require.NotNil(t, transfer.ReturnReason)
	assert.Equal(t, "insufficient_funds", *transfer.ReturnReason)

	var count int
	err = db.QueryRowContext(context.Background(),
		`SELECT COUNT(*) FROM ledger_entries WHERE transfer_id = $1`, id).Scan(&count)
	require.NoError(t, err)
	assert.Equal(t, 2, count, "return must create exactly 2 ledger entries (REVERSAL + RETURN_FEE)")
}

