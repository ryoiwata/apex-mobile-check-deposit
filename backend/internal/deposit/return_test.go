// TestReturn_ReasonsEndpoint_ReturnsAllCodes verifies that ReturnReasons contains
// all expected codes and that each entry is fully populated.
func TestReturn_ReasonsEndpoint_ReturnsAllCodes(t *testing.T) {
	expectedCodes := []string{
		"insufficient_funds", "account_closed", "stop_payment",
		"signature_mismatch", "stale_dated", "unable_to_locate", "frozen_account",
	}
	require.Len(t, models.ReturnReasons, len(expectedCodes),
		"ReturnReasons must contain exactly %d entries", len(expectedCodes))

	for i, r := range models.ReturnReasons {
		assert.Equal(t, expectedCodes[i], r.Code, "reason[%d] code mismatch", i)
		assert.NotEmpty(t, r.Label, "reason[%d] Label must not be empty", i)
		assert.NotEmpty(t, r.Description, "reason[%d] Description must not be empty", i)
	}
}

